# Funções
def escolha_sabor(sabor):
    while True:
        if sabor.lower() == 'cp' or sabor.lower() == 'ac':
            return sabor
        else:
            print('Escolha um sabor válido')
            sabor = input('Escolha o sabor (CP/AC): ').lower()

def escolha_tamanho(tamanho):
    while True:
        if tamanho.lower() == 'p' or tamanho.lower() == 'm' or tamanho.lower() == 'g':
            return tamanho
        else:
            print('Escolha um tamanho válido')
            tamanho = input('Escolha o tamanho (P/M/G): ').lower()

def pedido_inicial(sabor, tamanho):
    if sabor.lower() == 'cp':
        if tamanho.lower() == 'p':
            return 10.00
        elif tamanho.lower() == 'm':
            return 15.00
        elif tamanho.lower() == 'g':
            return 19.00
    elif sabor.lower() == 'ac':
        if tamanho.lower() == 'p':
            return 12.00
        elif tamanho.lower() == 'm':
            return 17.00
        elif tamanho.lower() == 'g':
            return 21.00

def adicionando_pedido(sabor, tamanho, pedidos):
    total_pedido = pedido_inicial(sabor, tamanho)
    #Metodo .append referência https://www.w3schools.com/python/ref_list_append.asp
    pedidos.append(total_pedido)
    while True:
        adicionar = input('Deseja pedir mais alguma coisa? (S/N): ').lower()
        if adicionar == 's':
            novo_sabor = input('Escolha o sabor (CP/AC): ').lower()
            novo_tamanho = input('Escolha o tamanho (P/M/G): ').lower()
            total_pedido = pedido_inicial(novo_sabor, novo_tamanho)
            pedidos.append(total_pedido)
        elif adicionar == 'n':
            break
        else:
            print('Escolha uma opção válida')

